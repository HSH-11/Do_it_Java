package sec01_datatype.EX02_NamingVariableAndConstant;

public class NamingVariableAndConstant {
	public static void main(String[] args) {
		//변수
		boolean aBcD;
		byte 가나다;
		short _abcd;
		char $ab_cd;
		// int 3abcd; 숫자 맨앞 X
		long abcd3;
		// float int; 자바 예약어는 사용 X
		double main;
		// int my Work; 스페이스, 특수 키는 사용할 수 없음
		String myClassName;
		int ABC;  // 전부 대문자로는 작성 권장 X
		
		// 상수
		final double PI;
		final int MY_DATA;
		final float myData; // 소문자 사용 권장 X
		
	}
}
